    public void getData(String title) throws SQLException, ClassNotFoundException, IOException {

        List<Anime> animeList = animeController.searchAnimeByTitle(title);
        if (!animeList.isEmpty()) {
            Anime anime = animeList.get(0); // Assuming you want the first result
            chagetext(
                anime.getAnimeTitle(),
                anime.getGenre(),
                String.valueOf(anime.getEpisodes()),
                String.valueOf(anime.getRating()),
                anime.getImg(),
                anime.getSnp(),
                anime.getStdname()
            );
            
        }

    }
    
     public void showImg(String Url) throws MalformedURLException, IOException {
        URL url = new URL(Url);
        Image image = ImageIO.read(url);

        ImageIcon imageIcon = new ImageIcon(image);
        jLabel1.setIcon(imageIcon);

     }

 


