   private void chagetext(String title, String genre, String eps, String rating,String Url, String snp, String Stdname) throws IOException {
        jLabel6.setText("Title : "+title);  // JLabel for anime title
        jLabel7.setText("Genre : "+genre);  // JLabel for genre
        jLabel9.setText("Episode : "+eps);    // JLabel for episode
        jLabel3.setText(rating); // JLabel for rating
        //jTextArea2.setText("Synopsis : "+snp); // JText fro synopsis
        jTextArea1.setText ("Synopsis : \n "+snp);
        jTextArea1.setLineWrap(true); // Enable word wrap
        jTextArea1.setWrapStyleWord(true);
        
        jTextArea1.setPreferredSize(new Dimension(567, 250));
//        jTextArea1.setPreferredSize(new Dimension(567, 216));
        jLabel10.setText("Studio : "+Stdname);
        
        showImg(Url); 
    }

